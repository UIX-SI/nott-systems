function makeSourceCode(company, mod) {
  if (mod.kind === "api") return mod.chat ? makeChatSource(company, mod) : makeApiSource(company, mod);
  if (mod.columns) return makeTableSource(company, mod);
  return makeFormSource(company, mod);
}


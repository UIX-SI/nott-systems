// Short monogram for the logo lockup (latin wordmark → 1–2 letters).
function markOf(company) {
  return company.logoText.length <= 2 ? company.logoText : company.logoText[0];
}

function brandBlock(company, mod) {
  return `// Brand tokens generated from nott's brand-token system (src/brand-tokens.js).
// Theming is driven by CSS variables — swap these to re-skin for any client.
const brand = {
  name: "${company.name}",
  logoText: "${company.logoText}",
  mark: "${markOf(company)}",
  module: { name: "${mod.name}", tagline: "${mod.tagline}" },
  tokens: ${JSON.stringify(company.tokens)},
};

const cssVars = {
  "--brand-primary": brand.tokens.primary,
  "--brand-on-primary": brand.tokens.onPrimary,
  "--brand-surface": brand.tokens.surface,
  "--brand-on-surface": brand.tokens.onSurface,
};

// Brand logo lockup: monogram tile + wordmark, themed via CSS vars.
function BrandMark() {
  return (
    <div style={{ display: "flex", alignItems: "center", gap: 8 }}>
      <span style={{ display: "inline-flex", width: 24, height: 24, alignItems: "center", justifyContent: "center", borderRadius: 6, background: "var(--brand-primary)", color: "var(--brand-on-primary)", fontWeight: 700, fontSize: 11, letterSpacing: "-0.02em" }}>{brand.mark}</span>
      <span style={{ color: "var(--brand-primary)", fontWeight: 700, fontSize: 16, letterSpacing: "-0.01em" }}>{brand.logoText}</span>
    </div>
  );
}`;
}

function componentNameOf(mod) {
  return mod.id.charAt(0).toUpperCase() + mod.id.slice(1) + "Preview";
}

// Request-body shape per chat module (matches each module's API contract).
function chatBodyExpr(mod) {
  if (mod.id === "slm") return "{ prompt: text }";
  if (mod.id === "rag") return "{ query: text, topK: 5 }";
  return '{ messages: [{ role: "user", content: text }] }';
}

function makeChatSource(company, mod) {
  const componentName = componentNameOf(mod);
  return `// Generated by nott — Easy modules for every SI project
// Package : ${mod.pkg}
// Client  : ${company.name} (${company.logoText})
// Module  : ${mod.name} (${mod.tagline})
// Endpoint: ${mod.endpoint}

import React from "react";

const API_ENDPOINT = "${mod.endpoint}";
const GREETING = "${mod.greeting}";

${brandBlock(company, mod)}

export default function ${componentName}() {
  const [messages, setMessages] = React.useState([{ role: "assistant", text: GREETING }]);
  const [input, setInput] = React.useState("");
  const [busy, setBusy] = React.useState(false);

  const send = async () => {
    const text = input.trim();
    if (!text || busy) return;
    setMessages((m) => [...m, { role: "user", text }]);
    setInput("");
    setBusy(true);

    let reply;
    try {
      const [method, path] = API_ENDPOINT.split(" ");
      const res = await fetch(path, {
        method,
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(${chatBodyExpr(mod)})
      });
      const data = await res.json();
      reply = data.reply || data.answer || data.content || JSON.stringify(data);
    } catch (e) {
      // No backend yet → demo fallback so the starter runs out of the box.
      reply = "(데모 응답) \\u201C" + text + "\\u201D — " + API_ENDPOINT + " 연결 시 실제 답변이 표시됩니다.";
    }
    setMessages((m) => [...m, { role: "assistant", text: reply }]);
    setBusy(false);
  };

  return (
    <main style={{ ...cssVars, minHeight: "100vh", background: "var(--brand-surface)", color: "var(--brand-on-surface)", padding: 32 }}>
      <section style={{ maxWidth: 560, margin: "0 auto", background: "white", borderRadius: 16, padding: 20, display: "flex", flexDirection: "column", height: 560 }}>
        <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center" }}>
          <BrandMark />
          <div style={{ fontSize: 11, color: "#94a3b8" }}>powered by nott</div>
        </div>
        <h1 style={{ fontSize: 20, marginTop: 12, fontWeight: 600 }}>{brand.module.name}</h1>
        <p style={{ fontSize: 12, color: "var(--brand-primary)", marginTop: 2, fontWeight: 600 }}>{brand.module.tagline}</p>

        <div style={{ flex: 1, overflowY: "auto", marginTop: 14, display: "flex", flexDirection: "column", gap: 8 }}>
          {messages.map((m, i) => (
            <div key={i} style={{ alignSelf: m.role === "user" ? "flex-end" : "flex-start", maxWidth: "85%" }}>
              <div style={{
                borderRadius: 12,
                padding: "8px 11px",
                fontSize: 13,
                lineHeight: 1.5,
                background: m.role === "user" ? "var(--brand-primary)" : "#f1f5f9",
                color: m.role === "user" ? "var(--brand-on-primary)" : "#0f172a"
              }}>
                {m.text}
              </div>
            </div>
          ))}
        </div>

        <div style={{ display: "flex", gap: 8, marginTop: 12 }}>
          <input
            value={input}
            onChange={(e) => setInput(e.target.value)}
            onKeyDown={(e) => e.key === "Enter" && send()}
            placeholder="메시지를 입력하세요…"
            style={{ flex: 1, border: "1px solid #e2e8f0", borderRadius: 10, padding: "10px 12px", fontSize: 13, outlineColor: "var(--brand-primary)" }}
          />
          <button onClick={send} disabled={busy} style={{ background: "var(--brand-primary)", color: "var(--brand-on-primary)", border: 0, borderRadius: 10, padding: "0 16px", fontWeight: 600, fontSize: 13, cursor: "pointer", opacity: busy ? 0.6 : 1 }}>
            전송
          </button>
        </div>
      </section>
    </main>
  );
}
`;
}

